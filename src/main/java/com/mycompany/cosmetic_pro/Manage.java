    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        
        String name =Pname.getText();
        String price =Price.getText();
        String cat=(String)Category.getSelectedItem();
             
        DB_CONNECTION db = new DB_CONNECTION();

        String InsertQuery = "insert into price_man(prod_name, category, price) values(?,?,?)";

        try {
            db.Pstmt = db.Con.prepareStatement(InsertQuery);
            db.Pstmt.setString(1, name);
            db.Pstmt.setString(2, cat);
            db.Pstmt.setString(3, price); 

            int i = db.Pstmt.executeUpdate();
            if (i > 0) {
                table_update(); 
                JOptionPane.showMessageDialog(this, "Price Successfully Added To Product");
            } else {
                JOptionPane.showMessageDialog(this, "Error In Adding Price To Product");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        } finally {
            try {
                if (db.Pstmt != null) db.Pstmt.close();
                if (db.Con != null) db.Con.close();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }

    }//GEN-LAST:event_addActionPerformed

