    private void table_update() {
        int CC;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/cosmetic","root","");
            insert = con1.prepareStatement("SELECT * FROM product");
            ResultSet Rs = insert.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) ProductTable.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
           
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("prod_id"));
                    v2.add(Rs.getString("prod_name"));
                    v2.add(Rs.getString("desc"));
                    v2.add(Rs.getString("category"));
                    v2.add(Rs.getString("qty"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        }
    }
     
