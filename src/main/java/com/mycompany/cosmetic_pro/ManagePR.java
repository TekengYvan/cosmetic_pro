    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
             String pname = Pname.getText();
              String pqty = PqtyInput.getText();
              String category = (String) Category.getSelectedItem();
              String description = Desc.getText();

             
        DB_CONNECTION db = new DB_CONNECTION();

        String InsertQuery = "insert into product(prod_name, qty, category, `desc`) values(?,?,?,?)";

        try {
            db.Pstmt = db.Con.prepareStatement(InsertQuery);
            db.Pstmt.setString(1, pname);
            db.Pstmt.setInt(2, Integer.parseInt(pqty)); // Use setInt() if qty is numeric
            db.Pstmt.setString(3, category);
            db.Pstmt.setString(4, description); // Avoid using "desc" as a variable name

            int i = db.Pstmt.executeUpdate();
            if (i > 0) {
                table_update(); 
                JOptionPane.showMessageDialog(this, "Product Successfully Added");
            } else {
                JOptionPane.showMessageDialog(this, "Error In Adding Product");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        } finally {
            try {
                if (db.Pstmt != null) db.Pstmt.close();
                if (db.Con != null) db.Con.close();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
            
    }//GEN-LAST:event_addActionPerformed

